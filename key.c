#include "rt.h"

int		key(int keycode, t_rt *rt)
{
	if (keycode == 53)
		exit(0);
	else if (keycode == 38)
	{
		rt->vh *= 1.1;
		rt->vw *= 1.1;
	}
	else if (keycode == 40)
	{
		rt->vh *= 0.5;
		rt->vw *= 0.5;
	}
	return (0);
}
